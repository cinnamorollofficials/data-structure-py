# Integer
age = 25
print("Integer:", age, "| Type:", type(age))
# Float
height = 12.7
print("Float:", height, "| Type:", type(height))

# String
name = "John"
print("String:", name, "| Type:", type(name))

# Boolean
is_student = True
print("Boolean:", is_student, "| Type:", type(is_student))

# Complex Number
z = 3 + 4j
print("Complex Number:", z, "| Type:", type(z))